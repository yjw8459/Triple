package Care.Lab;

public class TravelData {


        // 글번호
        private Integer no;

        // 여행제목
        private String title;

        // 여행내용
        private String contents;

        // 여행코
        private String travelcourse;

        // 여행테
        private String tema;

        // 여행지
        private String local;

        // 위도
        private String travelx;

        // 경도
        private String travely;

        public Integer getNo() {
            return no;
        }

        public void setNo(Integer no) {
            this.no = no;
        }

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }

        public String getContents() {
            return contents;
        }

        public void setContents(String contents) {
            this.contents = contents;
        }

        public String getTravelcourse() {
            return travelcourse;
        }

        public void setTravelcourse(String travelcourse) {
            this.travelcourse = travelcourse;
        }

        public String getTema() {
            return tema;
        }

        public void setTema(String tema) {
            this.tema = tema;
        }

        public String getLocal() {
            return local;
        }

        public void setLocal(String local) {
            this.local = local;
        }

        public String getTravelx() {
            return travelx;
        }

        public void setTravelx(String travelx) {
            this.travelx = travelx;
        }

        public String getTravely() {
            return travely;
        }

        public void setTravely(String travely) {
            this.travely = travely;
        }


    }

